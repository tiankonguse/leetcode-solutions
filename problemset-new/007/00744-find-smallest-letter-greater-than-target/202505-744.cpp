class Solution {
 public:
  char nextGreatestLetter(vector<char>& letters, char target) {
    int p = upper_bound(letters.begin(), letters.end(), target) - letters.begin();
    if (p == letters.size()) {
      return letters[0];
    }
    return letters[p];
  }
};
