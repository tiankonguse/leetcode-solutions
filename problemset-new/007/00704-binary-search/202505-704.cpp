class Solution {
 public:
  int search(vector<int>& nums, int target) {
    int p = lower_bound(nums.begin(), nums.end(), target) - nums.begin();
    if (p == nums.size() || nums[p] != target) {
      return -1;
    }
    return p;
  }
};

