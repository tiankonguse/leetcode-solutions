typedef long long ll;
class Solution {
 public:
  long long countCompleteDayPairs(vector<int>& hours) {
    ll ans = 0;
    vector<ll> cnt(24, 0);
    for (int h : hours) {
      h = h % 24;
      ans += cnt[(24 - h) % 24];
      cnt[h]++;
    }
    return ans;
  }
};

