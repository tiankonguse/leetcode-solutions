class Solution {
  vector<int> dp;
  int sum;
  int n;

  int Dfs(const int mask, const int t) {
    int& ret = dp[mask];
    if (ret != -1) return ret;
    // if (sum <= 0) return ret = 1;
    // if (mask == 0) return ret = 0;

    for (int i = 0; i < n; i++) {
      if ((1 << i) & mask == 0) continue;
      int num = i + 1;
      if (t + num >= sum) return ret = 1;  // 必胜
      if (Dfs(mask ^ (1 << i), t + num) == 0) {
        return ret = 1;
      }
    }
    return ret = 0;
  }

 public:
  bool canIWin(int n_, int sum_) {
    this->n = n_;
    this->sum = sum_;
    if(n * (1+n)/2 < sum) return false;
    int maxBit = 1 << n;
    dp.resize(maxBit, -1);
    return Dfs(maxBit - 1, 0);
  }
};

