class Solution {
 public:
  int residuePrefixes(string s) {
    set<char> S;
    int n = s.size();
    int ans = 0;
    for (int i = 1; i <= n; i++) {
      S.insert(s[i - 1]);
      if (i % 3 == S.size()) {
        ans++;
      }
    }
    return ans;
  }
};

