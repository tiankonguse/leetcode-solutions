class Solution {
 public:
  vector<int> minCosts(vector<int>& cost) {
    int n = cost.size();
    vector<int> ans = cost;

    int minCost = cost[0];
    for (int i = 0; i < n; i++) {
      minCost = min(minCost, cost[i]);
      ans[i] = minCost;
    }
    return ans;
  }
};

