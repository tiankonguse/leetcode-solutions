typedef long long ll;
class Solution {
 public:
  bool checkDivisibility(int n) {
    ll a = 0, b = 1;
    int nn = n;
    while (nn) {
      ll c = nn % 10;
      a += c;
      b *= c;
      nn /= 10;
    }
    return n % (a + b) == 0;
  }
};

