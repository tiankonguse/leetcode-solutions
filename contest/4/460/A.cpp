typedef long long ll;
class Solution {
 public:
  long long maximumMedianSum(vector<int>& nums) {
    sort(nums.begin(), nums.end());
    ll ans = 0;
    int l = 0, r = nums.size() - 1;
    while (l < r) {
      r--;
      ans += nums[r];
      r--;
      l++;
    }
    return ans;
  }
};

